void main() {
  // variable dengan var
  var name = "Alice";
  var age = 25;

  print("Nama: $name, Usia: $age");

  // Type Annotation
  String name1 = "Bob";
  int age1 = 30;
  print("Nama: $name1, Usia: $age1");

  //Multiple Variable
  String firstName, lastName; // Tipe data String
  firstName = "Charlie";
  lastName = "Brown";
  print("Nama Lengkap: $firstName $lastName");
}
